// Function to find maximum of two numbers
function max(num1, num2) {
    if (num1 > num2) {
      return num1;
    } else {
      return num2;
    }
  }
  
  // Function to find minimum of two numbers
  function min(num1, num2) {
    if (num1 < num2) {
      return num1;
    } else {
      return num2;
    }
  }
  